#include "flightStatus.h"


FLIGHT_STATUS_t flyStatus;






/**********************************************************************************************************
*函 数 名: SetCaliStatus
*功能说明: 设置传感器校准的状态
*形    参: 状态
*返 回 值: 无
**********************************************************************************************************/
void SetCaliStatus(uint8_t status)
{
	flyStatus.caliSt = status;
}

/**********************************************************************************************************
*函 数 名: GetCaliStatus
*功能说明: 获取传感器校准状态
*形    参: 无
*返 回 值: 状态
**********************************************************************************************************/
uint8_t GetCaliStatus(void)
{
    return flyStatus.caliSt;
}
